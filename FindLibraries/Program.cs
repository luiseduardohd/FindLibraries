		public static List<Type> getCustomTypes()
		{
			var assemblies = AppDomain.CurrentDomain.GetAssemblies();
			////HACK
			//var typesString = DotNetRuntimeTypesString + "\n" + DotNetTypesString;
			//string[] DotNetTypesNames = typesString.Split('\n');
